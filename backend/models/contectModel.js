import { Schema, model } from "mongoose";

const contectSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
});

const Contect = model("Contect", contectSchema);
export default Contect;
